package com.itheima;

public class Logger {
    /**
     * 日志级别
     *
     * 日志级别指的是日志信息的类型，日志都会分级别，常见的日志级别如下（级别由低到高）：
     *
     * +----------+------------------------------------------------------------------+------------------------+
     * | 日志级别  | 说明                                                              | 记录方式                |
     * +----------+------------------------------------------------------------------+------------------------+
     * | trace    | 追踪，记录程序运行轨迹【使用很少】                                   | log.trace("...")       |
     * | debug    | 调试，记录程序调试过程中的信息，实际应用中一般将其视为最低级别【使用较多】 | log.debug("...")       |
     * | info     | 记录一般信息，描述程序运行的关键事件，如：网络连接、io操作【使用较多】   | log.info("...")        |
     * | warn     | 警告信息，记录潜在有害的情况【使用较多】                              | log.warn("...")        |
     * | error    | 错误信息【使用较多】                                                | log.error("...")       |
     * +----------+------------------------------------------------------------------+------------------------+
     *
     * 日志级别说明：
     * - trace：最低级别，用于追踪程序的详细执行流程，通常只在开发调试时使用
     * - debug：调试级别，记录详细的调试信息，帮助开发人员定位问题
     * - info：信息级别，记录应用程序的关键运行信息和状态变化
     * - warn：警告级别，表示潜在的问题或异常情况，但不影响程序继续运行
     * - error：错误级别，记录错误和异常信息，表示程序出现了问题
     *
     * 在实际应用中，通常会配置一个最低日志级别，只有等于或高于该级别的日志才会被记录。
     * 例如：如果配置的日志级别为 info，则只会记录 info、warn 和 error 级别的日志。
     */
}
